def t(text, level=0, size=None, color=None, bold=False, center=False):
    p = doc.add_paragraph()
    if center: p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    sizes = {0: 20, 1: 15, 2: 12, 3: 11}
    run.font.size = Pt(size or sizes.get(level, 10))
    run.font.bold = bold or level <= 2
    if color: run.font.color.rgb = color
    run.font.name = '微软雅黑'
    run._element.rPr.rFonts.set(qn('w:eastAsia'), '微软雅黑')
    p.paragraph_format.space_before = Pt(12 if level <= 1 else 6)
    p.paragraph_format.space_after = Pt(4)

def card(num, name, floors, h, area, func, scene, equip, connect):
    """逐栋建筑卡片"""
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(18)
    r = p.add_run(f'B{num:02d}  |  {name}')
    r.font.size = Pt(13); r.font.bold = True
    r.font.name = '微软雅黑'; r._element.rPr.rFonts.set(qn('w:eastAsia'), '微软雅黑')

    info = doc.add_paragraph()
    ir = info.add_run(f'{floors}  |  高度约{h}m  |  建筑面积{area}平米  |  {connect}')
    ir.font.size = Pt(8.5); ir.font.color.rgb = RGBColor(0x99, 0x99, 0x99)
    ir.font.name = '微软雅黑'; ir._element.rPr.rFonts.set(qn('w:eastAsia'), '微软雅黑')

    for label, text in [('功能定位', func), ('场景画面', scene), ('设备配置', equip)]:
        pp = doc.add_paragraph()
        pp.paragraph_format.left_indent = Cm(0.5)
        r1 = pp.add_run(f'{label}：')
        r1.font.size = Pt(9.5); r1.font.bold = True
        r1.font.name = '微软雅黑'; r1._element.rPr.rFonts.set(qn('w:eastAsia'), '微软雅黑')
